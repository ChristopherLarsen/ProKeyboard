import UIKit


extension UIColor {
    
    static func thumperGrey () -> UIColor {
        return UIColor(red: 0.3, green: 0.3, blue: 0.3, alpha: 1.0)
    }

    static func thumperSmokyGlass () -> UIColor {
        return UIColor(red: 0.0, green: 0.0, blue: 0.0, alpha: 0.5)
    }
    
}